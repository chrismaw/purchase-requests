    {
        return collect(['data' => Task::with('createdByUser:id,name')->get()->map(function ($t){
            return [
                'DT_RowId' => 'row_' . $t->id,
                'task_number' => $t->number,
                'task_description' => $t->description,
                'task_active' => $t->is_active ? 'Yes' : 'No',
                'task_created_by' => $t->createdByUser->name
            ];
        })])->toJson();
    }

